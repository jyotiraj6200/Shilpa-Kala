let output = fileContent;

function insertTranslations() {
    const langs = ['en', 'hi', 'bh', 'mai', 'gu', 'mr', 'pa', 'kn', 'te', 'ta'];
    
    for (const lang of langs) {
        const regexStr = "  " + lang + ": \\{\\n([\\s\\S]*?)\\n  \\},?";
        const regex = new RegExp(regexStr, 'g');
        output = output.replace(regex, (match, p1) => {
            let additions = "";
            for (const [key, tObj] of Object.entries(newTranslations)) {
                // skip if already has it
                if (!p1.includes(key + ":")) {
                    const text = tObj[lang].replace(/"/g, '\\"');
                    additions += `\n    ${key}: "${text}",`;
                }
            }
            if (additions === "") return match;

            const closing = match.endsWith(',') ? '\n  },' : '\n  }';
            // append to the end of keys for this lang
            return `  ${lang}: {\n${p1},${additions.slice(0, -1)}${closing}`; 
        });
    }
}

insertTranslations();
fs.writeFileSync('src/i18n.ts', output);
